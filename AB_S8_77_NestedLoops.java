package com.company;

public class AB_S8_77_NestedLoops {
    public static void main(String[] args) {
        for (int i=1;i<=5;i++){
            System.out.println();
            for (int j=1;j<=5;j++){
                System.out.print(" ("+i+","+j+") ");
            }
        }
    }
}
