package com.company;

public class AB_S2_11_SizeOfDataType {
    public static void main(String[] args) {
//        int x;
//        Integer y;
//       For Integers :
        System.out.println("Int Min: " +Integer.MIN_VALUE);
        System.out.println("Int Max: " +Integer.MAX_VALUE);
        System.out.println("Int Byes: " +Integer.BYTES);

//        For Float :
        System.out.println("Int Min: " +Float.MIN_VALUE);
        System.out.println("Int Max: " +Float.MAX_VALUE);
        System.out.println("Int Byes: " +Float.BYTES);

//        For Byte:
        System.out.println("Int Min: " +Byte.MIN_VALUE);
        System.out.println("Int Max: " +Byte.MAX_VALUE);
        System.out.println("Int Byes: " +Byte.BYTES);

//        For Char :
        System.out.println("Int Min: " +Character.MIN_VALUE);
        System.out.println("Int Max: " +Character.MAX_VALUE);
        System.out.println("Int Byes: " +Character.BYTES);


    }
}
